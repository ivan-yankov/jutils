package yankov.jutils.functional;

public class RangeInt {
    private final int start;
    private final int end;

    public RangeInt(int start, int end) {
        this.start = start;
        this.end = end;
    }

    public int getStart() {
        return start;
    }

    public int getEnd() {
        return end;
    }
}
